public class Main {
    public static void main(String[] args) {
        System.out.print("Impresión con print:");
        System.out.print("Hola");
        System.out.print("Mucho gusto");
        System.out.print("Mi nombre es Omar");

        //Salto de línea
        System.out.print("\n\n");

        System.out.println("Impresión con print y caracter de escape:");
        System.out.print("Hola\n");
        System.out.print("Mucho gusto\n");
        System.out.print("Mi nombre es Omar\n");

        //Salto de línea
        System.out.print("\n");

        System.out.println("Impresión con println:");
        System.out.println("Hola");
        System.out.println("Mucho gusto");
        System.out.println("Mi nombre es Omar");
    }
}

